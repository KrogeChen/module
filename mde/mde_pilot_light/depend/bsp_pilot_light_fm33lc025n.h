//名称: 指示灯端口配置
//功能: 
//入口: 
//
//出口: 
//-------------------------------------------------------------------------------------------------
void bsp_pilot_light_cfg(void)
{
     FL_GPIO_InitTypeDef GPIO_InitStruct = {0};    
    
    //用到的GPIO引脚    设为输出 
    GPIO_InitStruct.pin        = FL_GPIO_PIN_15;
    GPIO_InitStruct.mode       = FL_GPIO_MODE_OUTPUT;
    GPIO_InitStruct.outputType = FL_GPIO_OUTPUT_PUSHPULL;
    GPIO_InitStruct.pull       = FL_DISABLE;
    GPIO_InitStruct.remapPin   = FL_DISABLE;	
    FL_GPIO_Init(GPIOA, &GPIO_InitStruct);
}
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//名称: 点亮指示灯
//功能: 
//入口: 灯编号
//
//出口: 无
//-------------------------------------------------------------------------------------------------
void bsp_pilot_light_lighten(sdt_int8u in_lamp_id)
{
    switch(in_lamp_id)
    {
        case 0:
        {
            FL_GPIO_SetOutputPin(GPIOA,FL_GPIO_PIN_15);
            break;
        }
        default:
        {
            break;
        }
    }
}
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//名称: 熄灭指示灯
//功能: 
//入口: 灯编号
//
//出口: 无
//-------------------------------------------------------------------------------------------------
void bsp_pilot_light_dark(sdt_int8u in_lamp_id)
{
    switch(in_lamp_id)
    {
        case 0:
        {
            FL_GPIO_ResetOutputPin(GPIOA,FL_GPIO_PIN_15);
            break;
        }
        default:
        {
            break;
        }
    }
}
